if [ -z "$1" ]; then
	echo "Пожалуйста, введите число для обратного отсчета."
	exit 1
fi

# Проверяем, является ли аргумент числом
if ! [[ "$1" =~ ^[0-9]+$ ]]; then
	echo "Ошибка: введите корректное число."
	exit 1
fi

# Выполняем обратный отсчет
for ((i=$1; i>=0; i--)); do
	echo "$i"
	sleep 1  # задержка в 1 секунду между числами
done
